<p><strong>Estil</strong>.</p>
<p>Utilitzarem expressions <u>impersonals</u> del  tipus: 112 rep una trucada on s’informa... evitarem expressions tipus Bombers  comenta o Bombers diu i utilitzarem Bombers informa o Bombers indica. S’evitarà  l’exposició dels fets en primera persona.<br />
