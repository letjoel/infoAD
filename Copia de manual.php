  Com s’ha fet fins ara, s’utilitzarà el tipus de lletra <strong>Arial 9</strong></p>
<p>&nbsp;</p>
<p><strong>Supervisió / coordinació.</strong></p>
<p>S’introdueixen els noms de  les persones que cobreixen el torn de spv + coordinació dividits per torns de  treball. En el cas que un supervisor o coordinador suplent treballi el torn  sencer, s’introdueix també en aquest camp. Els casos en els que un coordinador  suplent o gestor experimentat cobreixin coordinació en estones puntuals durant  el torn, ho introduirem a l’apartat d’incidències de personal.</p>
<p>&nbsp;</p>
<p><strong>Incidències de personal.</strong></p>
<p>Al camp d’ incidències de  personal, anotarem el número de persones que faltin respecte les que hi havia  previstes (no els noms de les persones), si algú es desconnecta per  indisposició, la gent que realitzi proves de validació de SEQUTAXI, si algú es  desconnecta per fer una reunió amb el coordinador per la presentació del clima  laboral,  reconduccions, etc...&nbsp;<br />
  També anotarem els casos  en els que coordinadors suplents o gestors experimentats cobreixin puntualment  el lloc de coordinació durant una estona. Si es cobreix el torn sencer, ja  s’indicarà en el camp de coordinació.</p>
<p>Qualsevol altre incidència  rellevant respecte el personal de sala, també s’anotarà.</p>
<p>&nbsp;</p>
<p><strong>Incidències tècniques.</strong></p>
<p>En aquest camp s’anotaran  totes les incidències tècniques que afectin al servei, tant a l’operativa com  als programes de supervisió. De totes elles es reportarà al tècnic informàtic  de guàrdia.</p>
<p>-Caigudes de passarel·la  de CME.<br />
  -No refresc de  l’aplicació.<br />
  -Qualsevol missatge d’  error que aparegui en l’aplicació.<br />
  -Intervencions tècniques  programades.<br />
  -Error en la CMS.<br />
  -Errors detectats a la  gravadora.<br />
  -Error a l’aplicatiu del  taxi o el taxi sala. En el cas del taxi sala, només es reportarà als tècnics  del servei si l’error és reiterat després de varis intents, donats els  problemes de cobertura de satèl·lit que existeixen en el centre de ZF.<br />
  -Problemes amb el correu  electrònic o fax.<br />
  -Problemes amb el mòbil de  sords / comunicació amb TSSO/D.O.<br />
  -Error en el despatx de  bombers.</p>
<p>S’informarà a TSSO/D.O. de  al detecció de l’error un cop s’hagi informat als tècnics informàtics. Es farà  un seguiment de la incidència tècnica i s’anotarà a l’informe totes les hores  exactes d’avisos als tècnics, trucades o sms de seguiment de la incidència i  finalització de la mateixa. A l’informe hauran de quedar clars tots aquests  punts.</p>
<p>&nbsp;</p>
<p><strong>Incidents rellevants.</strong></p>
<p>En aquest apartat s’hi  anotaran tots aquells incidents que han tingut una rellevància al servei  superior a la mitjana. Incidents que per les seves especials característiques,  afectació a persones o cossos operatius, afectació al servei, elevat número de  trucades relacionades, requereixen d’una especial atenció per part dels serveis  operatius</p>
<p>En aquests incidents  s’anotaran tots els avisos que se’n derivin a la resta de cossos operatius i  l’avís a TSSO i D.O. </p>
<p>Les especials  característiques dels incidents que s’introdueixen en aquest apartat de  l’informe acostumen a motivar avisos a TSSO/D.O. L’avís a TSSO/D.O. s’ha  d’anotar amb l’hora exacta de l’avís i anotar les instruccions que TSSO  faciliti per la gestió de l’incident. S’hauran d’anotar de manera exacta i  precisa totes les indicacions i totes les comunicacions que hi hagi amb TSSO de  seguiment i finalització de l’incident, anotant les hores exactes de totes les  comunicacions.</p>
